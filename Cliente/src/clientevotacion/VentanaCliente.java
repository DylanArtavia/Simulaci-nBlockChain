    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        // TODO add your handling code here:
        /**
         * Maneja el botón "Conectar".
         *
         * Intenta establecer una conexión con el servidor de votación. Si la
         * conexión se abre correctamente, se habilita el envío de votos.
         */
        try {
            conexion = new ClienteConexion("localhost", 5000);
            conectado = true;
            lblMensaje.setText("Conectado al servidor");
        } catch (Exception e) {
            conectado = false;
            lblMensaje.setText("Error al conectar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnConectarActionPerformed

    private void btnDesconectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesconectarActionPerformed
        // TODO add your handling code here:
        /**
         * Maneja el botón "Desconectar".
         *
         * Cierra la conexión existente con el servidor, libera recursos y
         * actualiza el estado de conexión.
         */
        if (conexion != null) {
            conexion.cerrar();
            conexion = null;
        }
        conectado = false;
        lblMensaje.setText("Desconectado del servidor");

    }//GEN-LAST:event_btnDesconectarActionPerformed

