    private void btnVotarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVotarActionPerformed
        // TODO add your handling code here:
        /**
         * Maneja el evento del botón "Votar".
         *
         * Valida la conexión al servidor, construye un voto con los datos
         * ingresados y lo envía mediante {@link ClienteConexion}. Luego muestra
         * el mensaje de respuesta recibido.
         */
        if (!conectado || conexion == null || !conexion.isAlive()) {
            lblMensaje.setText("No estás conectado al servidor");
            return;
        }

        try {
            String id = txtID.getText().trim();
            String token = txtToken.getText().trim();
            String candidato = comboCandidatos.getSelectedItem().toString();
            String fecha = java.time.LocalDateTime.now().toString();

            // Validaciones básicas
            if (id.isEmpty() || token.isEmpty() || candidato == null || candidato.isBlank()) {
                lblMensaje.setText("Completa ID, token y candidato.");
                return;
            }

            Votacion.Voto voto = new Votacion.Voto(id, token, candidato, fecha);

            String resultado = conexion.enviarVoto(voto);
            javax.swing.JOptionPane.showMessageDialog(this, resultado);
            lblMensaje.setText(resultado);

        } catch (Exception e) {
            lblMensaje.setText("Error al enviar voto: " + e.getMessage());
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnVotarActionPerformed

